# Write a program named age.py that includes someone's age and then calculates and reports the future age 10, 20, 30, and 40 years from now. 

age = 22
YEARS_INCREMENT = 10

print(f'You are {age} years old.')
print(f'In {YEARS_INCREMENT} years, you will be {age + YEARS_INCREMENT} years old.')
print(f'In {YEARS_INCREMENT + 10} years, you will be {age + YEARS_INCREMENT + 10} years old.')
print(f'In {YEARS_INCREMENT + 20} years, you will be {age + YEARS_INCREMENT + 20} years old.')
print(f'In {YEARS_INCREMENT + 30} years, you will be {age + YEARS_INCREMENT + 30} years old.')